
public class Round {

		private int roundNumber, score;

		public int getRoundNumber() {
			return roundNumber;
		}

		public void setRoundNumber(int roundNumber) {
			this.roundNumber = roundNumber;
		}

		public int getScore() {
			return score;
		}

		public void setScore(int score) {
			this.score = score;
		} 
}
